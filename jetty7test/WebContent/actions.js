// actions

function loginUsernameAction(msg) {
	$('#phrase').prop('type', 'text');
	renderMessage(msg);
}

function loginPasswordAction(msg) {
	$('#phrase').prop('type', 'password');
	renderMessage(msg);
}

function echoAction(msg) {
	$('#phrase').prop('type', 'text');
	renderMessage(msg);
}

function chatAction(msg) {
	$('#phrase').prop('type', 'text');
	renderMessage(msg);
}

